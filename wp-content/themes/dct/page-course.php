<ul>
<li>
<h3>多媒體網路(1)</h3>
</li>
</ul>
<blockquote>
<p>隔週上課。介紹多媒體資料在網路傳輸所需的技術，以及相關的協定和創新應用。</p>
</blockquote>
<ul>
<li>
<h3>資料庫概論(1)</h3>
</li>
</ul>
<blockquote>
