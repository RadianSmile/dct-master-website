</blockquote>
<h3> </h3>
<br />
<h3>傳播領域</h3>
<ul>
<li>
<h3>聲音基礎(1)</h3>
</li>
</ul>
<blockquote>
<p>由杜比工作室的杜篤之授課，透過老師曾配聲之電影來：1)探討聲音傳播本質；2)熟悉語言、音樂、聲音藝術聲音組構、混音等聲音創作技巧。 </p>
</blockquote>
<ul>
<li>
<h3>表演基礎(1)</h3>
</li>
</ul>
<blockquote>
<p>表演基礎課提供同學來學習表達部分「自己」的機會。同學應自課堂感受到公開表達自己的風範，認識「公開展演」的要求與條件。</p>
</blockquote>
<ul>
<li>
<h3>影像基礎(1)</h3>
</li>
</ul>
<blockquote>
<p>認識並掌握影像的影音語言特質和符號意義，以有效的分析、使用或製作它。</p>
</blockquote>
<ul>
<li>
<h3>文字基礎(1)</h3>
</li>
</ul>
<blockquote>
<p>訓練行文能簡潔達意，重心在知道怎麼避免贅字、贅辭、甚至贅段。</p>
</blockquote>
<ul>
<li>
<h3>圖像基礎(1)</h3>
</li>
</ul>
<blockquote>
<p>以平面為主軸，學習在廣告媒體上敘事。</p>
</blockquote>
<ul>
<li>
<h3>想像、敘事與互動基礎(3)</h3>
</li>
</ul>
<blockquote>
<p>想像：讓同學分享對想像的認知與創作，練習深化及擴張想像，展示作品及檢討所學。</p>
<p>敘事：敘事工具、故事與意義的結構、故事與意義的解構、故事的功能與實地考察。</p>
<p>互動：著重於基礎互動科技介紹，能製作與應用QR Code，以及運算美學的想像運用。</p>
</blockquote>
