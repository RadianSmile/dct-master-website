<h4>目前仍有開設之課程</h4>
<ul>
<li>
<h3>觸控介面設計(3)</h3>
</li>
</ul>
<blockquote>
<p>介紹人機互動(HCI)相關研究以及使用者介面(UI)設計原則，並以觸控式裝置為主要發展平台。需閱讀大量國內外相關研究文獻，以分組專題進行。</p>
</blockquote>
<ul>
<li>
<h3>互動設計專題：數位裝置實作(3)</h3>
</li>
</ul>
<blockquote>
<p>以Unity 3D軟體和Arduino的組合，教導學生單獨實作裝置雛型，製作完畢與並拍攝記錄影片。</p>
</blockquote>
<ul>
<li>
<h3>互動科技：媒材、感知與設計(3)</h3>
</li>
</ul>
<blockquote>
<p>授課方式涵蓋講授+技術學習+理論脈絡+實作開發，實際操作學習各式感測設備：Arduino與Processing溝通、Reactable…等，以分組實作進行。</p>
</blockquote>
<ul>
<li>
<h3>數位出版(3)</h3>
</li>
</ul>
<blockquote>
<p>認識數位出版產業發展及技術並建立數位出版實作能力，以分組設計實作數位出版作品。</p>
</blockquote>
<ul>
<li>
<h3>數位藝術中心專題實驗(3)</h3>
</li>
</ul>
<blockquote>
<p>以各種新媒體案例作為課堂補充講解，教導Unity3D在互動創作中之運用，以專題團隊合作企劃並完成一件互動作品。</p>
</blockquote>
<ul>
<li>
<h3>社群網路與應用(3)</h3>
</li>
</ul>
<blockquote>
<p>瞭解Web的基本運作原理與Web 2.0的概念與特色、社群媒體與線上社群服務的發展趨勢，學習開發網路社群應用程式。</p>
</blockquote>
<ul>
<li>
<h3>數位文創設計(3)</h3>
</li>
</ul>
<blockquote>
<p>發展數位文創設計: 學習設計文創商品、建立行動平台、將科技融入設計中，以分組建立設計團隊小組。 </p>
</blockquote>
<ul>
<li>
<h3>社群媒體概論(3)</h3>
</li>
</ul>
<blockquote>
<p>針對欲從事社群媒體設計與研究之人才需求，幫助學生瞭解社群媒體內容評析、訊息散佈機制、網路社群分析與人機互動設計等不同的面向。</p>
</blockquote>
<ul>
<li>
<h3>展演實務與媒體策略(3)</h3>
</li>
</ul>
<blockquote>
<p>學習媒體創作與策展計劃之提案、新媒體平台的策略運用與商業模式分析 、展演的空間規劃與實際呈現。</p>
</blockquote>
